package com.egoal.darkestpixeldungeon.actors.mobs.npcs;

import com.egoal.darkestpixeldungeon.Dungeon;
import com.egoal.darkestpixeldungeon.Journal;
import com.egoal.darkestpixeldungeon.actors.Damage;
import com.egoal.darkestpixeldungeon.actors.buffs.Buff;
import com.egoal.darkestpixeldungeon.effects.CellEmitter;
import com.egoal.darkestpixeldungeon.effects.particles.ElmoParticle;
import com.egoal.darkestpixeldungeon.items.Heap;
import com.egoal.darkestpixeldungeon.messages.Messages;
import com.egoal.darkestpixeldungeon.scenes.GameScene;
import com.egoal.darkestpixeldungeon.windows.WndBag;
import com.egoal.darkestpixeldungeon.windows.WndTradeItem;
import com.egoal.darkestpixeldungeon.items.Item;
import com.egoal.darkestpixeldungeon.sprites.ShopkeeperSprite;

public class Shopkeeper extends NPC {

  {
    spriteClass = ShopkeeperSprite.class;

    properties.add(Property.IMMOVABLE);
  }

  @Override
  protected boolean act() {

    throwItem();

    sprite.turnTo(pos, Dungeon.hero.pos);
    spend(TICK);
    return true;
  }

  @Override
  public int takeDamage(Damage dmg) {
    flee();
    return 0;
  }

  @Override
  public void add(Buff buff) {
    flee();
  }

  public void flee() {
    for (Heap heap : Dungeon.level.getHeaps().values()) {
      if (heap.type == Heap.Type.FOR_SALE) {
        CellEmitter.get(heap.pos).burst(ElmoParticle.FACTORY, 4);
        heap.destroy();
      }
    }

    destroy();

    sprite.killAndErase();
    CellEmitter.get(pos).burst(ElmoParticle.FACTORY, 6);
  }

  @Override
  public boolean reset() {
    return true;
  }

  public static WndBag sell() {
    return GameScene.selectItem(itemSelector, WndBag.Mode.FOR_SALE, Messages
            .get(Shopkeeper.class, "sell"));
  }

  private static WndBag.Listener itemSelector = new WndBag.Listener() {
    @Override
    public void onSelect(Item item) {
      if (item != null) {
        WndBag parentWnd = sell();
        GameScene.show(new WndTradeItem(item, parentWnd));
      }
    }
  };

  @Override
  public boolean interact() {
    sell();
    return false;
  }
}
